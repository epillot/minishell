#include "libft.h"

int		nb_size(intmax_t nb)
{
	int	ret;

	ret = 1;
	if (nb < 0)
		ret++;
	while (nb >= 10 || nb <= -10)
	{
		nb /= 10;
		ret++;
	}
	return (ret);
}
