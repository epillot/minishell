#include "libft.h"

char		*ft_strdup(const char *src)
{
	char	*dest;
	size_t	i;

	i = 0;
	dest = ft_strnew(sizeof(char) * ft_strlen(src));
	if (dest == NULL)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
